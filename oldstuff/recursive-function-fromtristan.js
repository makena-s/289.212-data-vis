function countFamilies(superfamily) {
    // there are ways to reapproach and optimise this
    // it's a bit verbose, but relatively easy to adapt/read/understand
    // you can change the if condition to count different things, perhaps controlled by the function params
    result = 0;

    Object.keys(beetledata["order-Coleoptera"]).forEach(orderkey => {
        console.log('suborder: ' + orderkey);

        Object.keys(beetledata["order-Coleoptera"][orderkey]).forEach(infrakey => {
            console.log('infraorder: ' + infrakey);

            Object.keys(beetledata["order-Coleoptera"][orderkey][infrakey]).forEach(cladekey => {
                console.log('clade: ' + cladekey);

                Object.keys(beetledata["order-Coleoptera"][orderkey][infrakey][cladekey]).forEach(superfamkey => {
                    console.log('superfamily: ' + superfamkey);

                    beetledata["order-Coleoptera"][orderkey][infrakey][cladekey][superfamkey].forEach(family => {
                        console.log('family: ' + family);

                        if (superfamkey == superfamily) {
                            document.write(family + '<br />');
                            result ++;
                        }
                    });
                });
            });
        });
    });

    return result;
}

document.write(countFamilies('superfamily-Asiocoleoidea'));
